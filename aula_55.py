# Split, Strip e Join

# A função split() divide as palavras de uma string com base no parâmetro que é passado

string = "Uma frase qualquer"
palavras = string.split()
print(palavras)

# A função strip() remove os espaços antes e depois da string

string = " Uma frase qualquer "
print(string.strip())

# A função join() cria um separador entre cada índice de uma string ou lista

string = "abcde"
print("-".join(string))



