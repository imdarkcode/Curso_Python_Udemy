"""
Sets: Conjuntos em Python
Ele é um conjunto de valores que não possui index e não possuem uma ordem certa
"""

# Criando um tipo de dado set

conjunto_1 = set("Otávio")
conjunto_2 = {"O", "t", "á", "v", "i", "o"}
print(conjunto_1)
print(conjunto_2)

# Por padrão eles removem valores duplicados

conjunto_3 = {1, 2, 3, 3, 3, 4, 1}
print(conjunto_3)