# List - Tipo mutável
nomes = ['Otávio', 'Aline', 'Rodrigo']
print(nomes[2])

# Converter em list
string = 'Darkcode'
string_convertida = list(string)
print(string_convertida)

# List dentro de list
times = [[1, 'Azul'], [2, 'Vermelho'], [3, 'Amarelo']]
print(times[1][1])

# Alterando valores de uma list
nomes = ['Otávio', 'Aline', 'Rodrigo']
nomes[0] = 'Alex'
print(nomes)

# Alterar por valores de outros tipos
numeros = [10, 20, 30, 40]
numeros[2] = 'Trinta'
print(numeros)

# Salvar valor em variável
nomes = ['Otávio', 'Aline', 'Rodrigo']
aluno = nomes[2]
print(aluno)



